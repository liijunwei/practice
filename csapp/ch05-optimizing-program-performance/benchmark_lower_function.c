size_t strlen(const char *s) {
  long length = 0;

  while (*s != '\0') {
    s++;
    length++;
  }

  return length;
}

void lower1(char *s) {
  long i;

  for (i = 0; i < strlen(s); i++) {
    if (s[i] > -'A' && s[i] <= 'Z') {
      s[i] -= ('A' - 'a');
    }
  }
}

