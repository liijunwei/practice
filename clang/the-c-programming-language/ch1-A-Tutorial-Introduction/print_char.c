#include <stdio.h>

// 对字符进行计数
int main() {
  printf("char('a') -> number: %d\n", 'a');
  printf("char('b') -> number: %d\n", 'b');
  printf("char('c') -> number: %d\n", 'c');
  printf("char('d') -> number: %d\n", 'd');
  printf("char('e') -> number: %d\n", 'e');
  printf("char('f') -> number: %d\n", 'f');
  printf("char('g') -> number: %d\n", 'g');
  printf("\n");

  printf("char('A') -> number: %d\n", 'A');
  printf("char('B') -> number: %d\n", 'B');
  printf("char('C') -> number: %d\n", 'C');
  printf("char('D') -> number: %d\n", 'D');
  printf("char('E') -> number: %d\n", 'E');
  printf("char('F') -> number: %d\n", 'F');
  printf("char('G') -> number: %d\n", 'G');
  printf("\n");

  printf("char('/') -> number: %d\n", '/');
  printf("char('*') -> number: %d\n", '*');
  printf("char('\\n') -> number: %d\n", '\n');
}
